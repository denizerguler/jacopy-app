"""
Triangular Lie bialgebroid ``(TM, T*M)`` library wrapper.

A Lie bialgebroid ``(A, A^*)`` over a manifold ``M`` is a pair of Lie
algebroids on dual bundles whose differentials are derivations of each
other's bracket. The **triangular** case is the special instance where
the Lie algebroid structure on ``A^* = T^*M`` is induced by a Poisson
bivector ``π``: the bracket on ``T^*M`` is the classical Koszul bracket

    [α, β]_{T^*M} = L_{π^#α} β − L_{π^#β} α − d⟨π^#α, β⟩,

the anchor is the sharp musical map ``π^♯: T^*M → TM``, and the
algebroid Cartan calculus is the *tilde* calculus
``(d̃, L̃, ι̃)`` whose operators carry the bialgebroid's own
algebroid name so downstream proofs can keep the Q3.1.6-style mixed
identities readable.

This wrapper bundles all that data into a single object so the
Liu-Weinstein-Xu Courant bracket on ``TM ⊕ T*M`` (Stage F.2 of Faz 9)
can be built from one hand-off:

    TLB = TriangularLieBialgebroid(pi)
    C   = CourantAlgebroid(bialgebroid=TLB)

The wrapper deliberately stays operator-level — it does not construct a
:class:`~jacopy.library.lie_algebroid.LieAlgebroid` instance for the
``T^*M`` side, since the Sharp musical map (``π^♯``) is a
:class:`~jacopy.algebra.derivation.Derivation` that is *not* an
:class:`~jacopy.calculus.anchor.Anchor` subclass; the existing
``LieAlgebroid`` constructor type-checks for the latter. Following
:class:`~jacopy.library.poisson.PoissonBracket`'s precedent, we expose
the underlying operators directly (``koszul``, ``tilde_d``,
``tilde_lie_derivative``, ``tilde_interior``, ``sharp``) instead.
"""

from __future__ import annotations

from typing import Callable, Optional

from jacopy.algebra.derivation import Derivation
from jacopy.brackets.base import GradedBracket
from jacopy.brackets.koszul import KoszulBracket
from jacopy.brackets.lie import LieBracket, lie as default_lie_bracket
from jacopy.calculus.exterior_d import (
    ExteriorDerivative,
    d as default_d,
)
from jacopy.calculus.interior import (
    InteriorProduct,
    interior as default_interior,
)
from jacopy.calculus.lie_derivative import (
    LieDerivative,
    lie_derivative as default_lie_derivative,
)
from jacopy.calculus.musical import Sharp
from jacopy.core.expr import Expr


LieDerivativeFactory = Callable[[Expr], LieDerivative]
InteriorFactory = Callable[[Expr], InteriorProduct]


class TriangularLieBialgebroid:
    """``(TM, T^*M)``, a triangular Lie bialgebroid generated by a Poisson bivector.

    Parameters
    ----------
    pi
        The Poisson bivector ``π``. Treated symbolically; the wrapper
        does not enforce ``[π, π]_SN = 0`` here (callers verify the
        Poisson condition separately, e.g. via
        :class:`~jacopy.library.poisson.PoissonBracket.prove_jacobi_reduction`
        or by adding the assumption to a registry).
    name
        Optional display name; defaults to
        ``"TLB(TM, T*M; π=...)"``.

    Notes
    -----
    The bialgebroid bundles two compatible Lie-algebroid sides:

    * **TM side** — canonical: bracket is the standard Lie bracket on
      vector fields; Cartan calculus is the smooth-manifold default
      ``(d, L_X, ι_X)``. The wrapper exposes these via
      :attr:`tm_bracket`, :attr:`tm_d`, :attr:`tm_lie_derivative`,
      :attr:`tm_interior` so the caller can read them off without
      having to import the singletons separately.
    * **T*M side** — induced by ``π``: bracket is the classical Koszul
      bracket (with anchor ``π^♯``); Cartan calculus uses a fresh
      :class:`~jacopy.calculus.exterior_d.ExteriorDerivative` named
      ``d̃`` plus tilde-tagged Lie-derivative / interior factories
      whose operator names carry the tilde mark. These read as
      :attr:`koszul`, :attr:`tilde_d`, :attr:`tilde_lie_derivative`,
      :attr:`tilde_interior`.

    The musical map ``π^♯: T^*M → TM`` is built once and reused; both
    the Koszul bracket and the LWX Courant anchor (Stage F.2) read it
    off via :attr:`sharp`.
    """

    __slots__ = (
        "_pi",
        "_sharp",
        "_tm_bracket",
        "_tm_d",
        "_tm_lie_derivative",
        "_tm_interior",
        "_koszul",
        "_tilde_d",
        "_tilde_lie_derivative",
        "_tilde_interior",
        "_name",
    )

    def __init__(
        self,
        pi: Expr,
        *,
        name: Optional[str] = None,
    ) -> None:
        if not isinstance(pi, Expr):
            raise TypeError("TriangularLieBialgebroid requires an Expr π")
        self._pi = pi
        self._sharp = Sharp(pi)

        # TM side — canonical, no per-instance allocation needed.
        self._tm_bracket = default_lie_bracket
        self._tm_d = default_d
        self._tm_lie_derivative = default_lie_derivative
        self._tm_interior = default_interior

        # T*M side — Koszul + tilde-calculus.
        self._koszul = KoszulBracket(
            self._sharp,
            name="[·,·]_{T*M}",
        )
        # The tilde-side d̃ is a *fresh* ExteriorDerivative (not the
        # default singleton) so that mixed-identity rewrites can
        # distinguish ``d`` (TM-side) from ``d̃`` (T*M-side) at the
        # operator-name level.
        self._tilde_d = ExteriorDerivative(name="d̃")
        self._tilde_interior = self._make_tilde_interior_factory()
        self._tilde_lie_derivative = self._make_tilde_lie_factory()

        self._name = (
            name
            if name is not None
            else f"TLB(TM, T*M; π={pi._repr_inner()})"
        )

    # ---- factories -------------------------------------------------- #

    def _make_tilde_interior_factory(self) -> InteriorFactory:
        """Return ``ω → ι̃_ω``, the T*M-side interior product factory."""

        def factory(omega: Expr) -> InteriorProduct:
            if not isinstance(omega, Expr):
                raise TypeError("tilde-ι factory requires an Expr 1-form")
            return default_interior(omega, name=f"ι̃_{omega._repr_inner()}")

        return factory

    def _make_tilde_lie_factory(self) -> LieDerivativeFactory:
        """Return ``ω → L̃_ω``, the T*M-side Lie-derivative factory.

        The factory builds a :class:`LieDerivative` whose name carries
        the tilde mark and whose ``d`` / ``iota_factory`` plumbing
        is wired to the bialgebroid's own ``d̃`` / ``ι̃`` so the
        magic-formula expansion stays inside the T*M Cartan family.
        """
        tilde_d = self._tilde_d
        iota_factory = self._tilde_interior

        def factory(omega: Expr) -> LieDerivative:
            if not isinstance(omega, Expr):
                raise TypeError("tilde-L factory requires an Expr 1-form")
            return default_lie_derivative(
                omega,
                name=f"L̃_{omega._repr_inner()}",
                d=tilde_d,
                iota_factory=iota_factory,
            )

        return factory

    # ---- accessors -------------------------------------------------- #

    @property
    def pi(self) -> Expr:
        return self._pi

    @property
    def sharp(self) -> Sharp:
        """``π^♯: T^*M → TM``, the musical map induced by π."""
        return self._sharp

    @property
    def name(self) -> str:
        return self._name

    # TM side --------------------------------------------------------- #

    @property
    def tm_bracket(self) -> GradedBracket:
        """``[·,·]_{TM}``, the standard Lie bracket on vector fields."""
        return self._tm_bracket

    @property
    def tm_d(self) -> ExteriorDerivative:
        """``d``, the TM-side exterior derivative (singleton)."""
        return self._tm_d

    @property
    def tm_lie_derivative(self) -> LieDerivativeFactory:
        """``X → L_X``, the TM-side Lie-derivative factory."""
        return self._tm_lie_derivative

    @property
    def tm_interior(self) -> InteriorFactory:
        """``X → ι_X``, the TM-side interior-product factory."""
        return self._tm_interior

    # T*M side -------------------------------------------------------- #

    @property
    def koszul(self) -> KoszulBracket:
        """``[·,·]_{T^*M}``, the classical Koszul bracket on 1-forms."""
        return self._koszul

    @property
    def tilde_d(self) -> ExteriorDerivative:
        """``d̃``, the T*M-side exterior derivative (fresh)."""
        return self._tilde_d

    @property
    def tilde_lie_derivative(self) -> LieDerivativeFactory:
        """``ω → L̃_ω``, the T*M-side Lie-derivative factory."""
        return self._tilde_lie_derivative

    @property
    def tilde_interior(self) -> InteriorFactory:
        """``ω → ι̃_ω``, the T*M-side interior-product factory."""
        return self._tilde_interior

    # ---- dunder ----------------------------------------------------- #

    def __repr__(self) -> str:
        return f"TriangularLieBialgebroid(π={self._pi._repr_inner()})"


def triangular_lie_bialgebroid(
    pi: Expr,
    *,
    name: Optional[str] = None,
) -> TriangularLieBialgebroid:
    """Functional mirror of :class:`TriangularLieBialgebroid`."""
    return TriangularLieBialgebroid(pi, name=name)
